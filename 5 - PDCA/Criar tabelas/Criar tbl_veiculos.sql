CREATE TABLE `tbl_veiculos` (
  `ID_VEIC` int NOT NULL AUTO_INCREMENT,
  `NREDS` varchar(18) NOT NULL,
  `ORG_UN_REG` varchar(30) DEFAULT NULL,
  `UN_AREA_MIL` varchar(50) DEFAULT NULL,
  `UN_REGISTRO` varchar(100) DEFAULT NULL,
  `COD_DIAO` varchar(6) DEFAULT NULL,
  `T_C` varchar(9) DEFAULT NULL,
  `DATA_FATO` date DEFAULT NULL,
  `HORA_FATO` time DEFAULT NULL,
  `MUNICIPIO` varchar(50) DEFAULT NULL,
  `SITUACAO_VEICULO` varchar(20) DEFAULT NULL,
  `TIPO_VEICULO` varchar(100) DEFAULT NULL,
  `ANO_MODELO` varchar(20) DEFAULT NULL,
  `MARCA_MODELO` varchar(40) DEFAULT NULL,
  `QTDE_VEIC` int DEFAULT NULL,
  PRIMARY KEY (`ID_VEIC`),
  KEY `fk_veic_NREDS` (`NREDS`),
  CONSTRAINT `fk_veic_NREDS` FOREIGN KEY (`NREDS`) REFERENCES `tbl_ocorrencias` (`NREDS`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4